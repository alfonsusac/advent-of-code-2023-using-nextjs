export default function Page() {
  const input = puzzle
  const rows = input.split('\n')
  const map = new Array(rows.length).fill([]).map((row, i) => rows[i].split(''))
  // console.log(map)
  let load = 0
  const freeSpace: number[] = (new Array(map[0].length)).fill(0)
  for (let y = 0; y < map.length; y++) {
    const rowLn = map[0].length
    const row = map[y]
    for (let x = 0; x < rowLn; x++) {
      if (row[x] === '.') {
        freeSpace[x] += 1
      }
      if (row[x] === 'O') {
        map[y - freeSpace[x]][x] = 'O'
        if(freeSpace[x] > 0) map[y][x] = '.'
        load += map.length - (y - freeSpace[x])
        // console.log(`O at ${y - freeSpace[x]} has load of ${map.length - (y-freeSpace[x])}`)
      }
      if (row[x] === '#') {
        freeSpace[x] = 0
      }
    }
  }
  // printMap(map)
  console.log(load)
}

