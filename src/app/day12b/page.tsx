const cache: {[key: string]: number} = {}

function count(config: string, pattern: number[]) {
  // console.log(`${config} ${pattern}`)
  const first = config[0]

  if (!first) {
    if (pattern.length === 0) return 1; else return 0
  }
  if (pattern.length === 0) {
    if (config.includes('#')) return 0; else return 1
  }

  const key = JSON.stringify({ config, pattern })
  if (key in cache)
    return cache[key]

  let ways = 0
  if (first === '?' || first === ".") {
    ways += count(config.slice(1), [...pattern])
  }
  if (first === '?' || first === "#") {
    if (pattern[0] <= config.length && !config.slice(0, pattern[0]).includes('.') && (config.length === pattern[0] || config[pattern[0]] !== '#')) {
      ways += count(config.slice(pattern[0] + 1), [...pattern.slice(1)])
    }
  }

  cache[key] = ways
  return ways
}


